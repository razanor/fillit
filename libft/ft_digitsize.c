int		ft_digitsize(int n)
{
	int			i;

	i = n < 0 ? 1 : 0;
	if (n < 0)
		n = -n;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
