int	ft_count_words(char const *s, char c)
{
	int	i;

	i = 0;
	while (*s)
	{
		if (*s == c)
			s++;
		else
		{
			if (*(s + 1) == c || *(s + 1) == '\0')
				i++;
			s++;
		}
	}
	return (i);
}
