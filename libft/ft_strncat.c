#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	lens1;

	lens1 = ft_strlen(s1);
	while (*s2 && n)
	{
		s1[lens1] = *s2;
		lens1++;
		s2++;
		n--;
	}
	s1[lens1] = '\0';
	return (s1);
}
